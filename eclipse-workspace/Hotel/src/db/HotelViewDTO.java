package db;

public class HotelViewDTO {
	
	private String name;
	private int price;
	private String img;
	private int dday;
	private String rday;// 체크인
	private int mfood;
	private int dfood;
	private int service;
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getDday() {
		return dday;
	}
	public void setDday(int dday) {
		this.dday = dday;
	}
	public String getRday() {
		return rday;
	}
	public void setRday(String rday) {
		this.rday = rday;
	}
	public int getMfood() {
		return mfood;
	}
	public void setMfood(int mfood) {
		this.mfood = mfood;
	}
	public int getDfood() {
		return dfood;
	}
	public void setDfood(int dfood) {
		this.dfood = dfood;
	}
	public int getService() {
		return service;
	}
	public void setService(int service) {
		this.service = service;
	}
	
	
	
	
	
	
}
